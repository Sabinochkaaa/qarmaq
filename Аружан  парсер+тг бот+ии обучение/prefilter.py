"""
prefilter.py
Быстрая проверка по ключевым словам/паттернам ДО обращения к Groq.
Если ничего не сработало — сообщение считается чистым без затрат на AI-запрос.
Если сработало хотя бы одно правило — сообщение идёт на полный анализ.

Списки умышленно держим общими (без конкретных сленговых деталей одной из
категорий), финальную семантическую классификацию и risk_score всегда
выставляет Groq — префильтр только решает "анализировать или нет".
"""

import re

KEYWORD_GROUPS = {
    "пирамида": [
        r"пассивн\w* доход", r"удво[ий]м", r"вложи.{0,15}получ", r"реферальн\w* программ",
        r"инвестиц\w* без риск", r"гарантирован\w* доход", r"x2\b", r"x3\b",
        r"заработ\w* (без вложен|с телефона|от \d+ ?(тыс|т\.р|к)\b)",
    ],
    "дроп": [
        r"\bдроп\w*", r"обнал\w*", r"скупк\w* карт", r"реквизит\w* нужн",
        r"карт\w* для (вывода|обнала)", r"нужны карты банк",
    ],
    "наркотики": [
        r"\bклад\w*", r"\bзакладк\w*", r"24/?7 доставка",
        r"шишк\w*", r"\bстаф\w*",
    ],
    "казино": [
        r"казино", r"\bслот\w*\b", r"ставк\w* на спорт", r"без верификации",
        r"casino", r"бездепозитн\w* бонус", r"крутить барабан",
    ],
    "утечка_бд": [
        r"проби[вй]\w*", r"слив (бд|базы|данных)", r"\bбд\b.{0,15}(прода|купи)",
        r"продам (бд|базу)", r"по номеру телефона найд", r"полные данные по человеку",
        r"паспортн\w* данн\w* (прода|купи|узна)",
    ],
    # общие "красные флаги", не привязанные к одной категории
    "общие_флаги": [
        r"требуется (паспорт|карта|снилс|реквизит)", r"скинь (карту|реквизит|паспорт)",
        r"анонимно и без последствий", r"только сегодня.{0,10}акция",
        r"пиши в (лс|директ|телеграм) для подробностей",
    ],
}

COMPILED_GROUPS = {
    cat: [re.compile(p, re.IGNORECASE) for p in patterns]
    for cat, patterns in KEYWORD_GROUPS.items()
}


def check_message(normalized_text: str):
    """
    Возвращает (is_candidate: bool, matched_groups: list[str])
    matched_groups — какие категории-кандидаты подсветил префильтр
    (это только подсказка для промпта, не финальное решение).
    """
    if not normalized_text:
        return False, []

    matched = []
    for category, patterns in COMPILED_GROUPS.items():
        for pattern in patterns:
            if pattern.search(normalized_text):
                matched.append(category)
                break

    return (len(matched) > 0), matched


if __name__ == "__main__":
    test_texts = [
        "Продам бд по номеру телефона, пиши в лс",
        "Привет, как дела, что нового?",
        "Казино без верификации, бездепозитный бонус для новых игроков",
    ]
    for t in test_texts:
        print(t, "->", check_message(t))
